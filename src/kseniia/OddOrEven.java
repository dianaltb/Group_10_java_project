package kseniia;

public class OddOrEven {

    public static void main(String[] args) {

        int num = 5;



        if(num % 2 == 0){
            System.out.println("Number " + num + " is even");
        }else if(num % 2 != 0){
            System.out.println("Number " + num + " is odd");
        }

    }
}


